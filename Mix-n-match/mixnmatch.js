let isSelected = "";

function changeOpacity(clicked) {
  if (clicked == "triangle") {
    document.querySelector(".triangle").style.opacity = "0.6";
    document.querySelector(".circle").style.opacity = "1";
    document.querySelector(".rectangle").style.opacity = "1";
    document.querySelector(".heart").style.opacity = "1";
    isSelected = "triangle";
  }
  if (clicked == "circle") {
    document.querySelector(".triangle").style.opacity = "1";
    document.querySelector(".circle").style.opacity = "0.6";
    document.querySelector(".rectangle").style.opacity = "1";
    document.querySelector(".heart").style.opacity = "1";
    isSelected = "circle";
  }
  if (clicked == "rectangle") {
    document.querySelector(".triangle").style.opacity = "1";
    document.querySelector(".circle").style.opacity = "1";
    document.querySelector(".rectangle").style.opacity = "0.6";
    document.querySelector(".heart").style.opacity = "1";
    isSelected = "rectangle";
  }
  if (clicked == "heart") {
    document.querySelector(".triangle").style.opacity = "1";
    document.querySelector(".circle").style.opacity = "1";
    document.querySelector(".rectangle").style.opacity = "1";
    document.querySelector(".heart").style.opacity = "0.6";
    isSelected = "heart";
  }
}

